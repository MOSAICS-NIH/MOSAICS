
///////////////////////////////////////////////////////////////////////////////////////////////////////////////
//                                                                                                           //
// This function takes in two vectors and returns their dot product                                          //
//                                                                                                           //
///////////////////////////////////////////////////////////////////////////////////////////////////////////////
double dot(double a[3],double b[3])
{
    return a[0]*b[0] + a[1]*b[1] + a[2]*b[2];
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////
//                                                                                                           //
// This function takes in two vectors and returns their cross product                                        //
//                                                                                                           //
///////////////////////////////////////////////////////////////////////////////////////////////////////////////
vector<double> cross(vector<double> &A,vector<double> &B)
{
    vector<double> cross(3,0.0);

    cross[0] = (A[1]*B[2] - A[2]*B[1]);
    cross[1] = (A[2]*B[0] - A[0]*B[2]);
    cross[2] = (A[0]*B[1] - A[1]*B[0]);

    return cross;
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////
//                                                                                                           //
// This function takes in two vectors and returns their dot product                                          //
//                                                                                                           //
///////////////////////////////////////////////////////////////////////////////////////////////////////////////
double dotp(vector<double> &a,vector<double> &b)
{
    return a[0]*b[0] + a[1]*b[1] + a[2]*b[2];
}

