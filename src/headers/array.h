///////////////////////////////////////////////////////////////////////////////////////////////////////////////
//                                                                                                           //
// This function maps a 4-d array into a 1-d array                                                           //
//                                                                                                           //
///////////////////////////////////////////////////////////////////////////////////////////////////////////////
int map4(int i,int j,int k,int l, int max_i,int max_j,int max_k,int max_l)
{
    return (l*max_i*max_j*max_k) + (k*max_i*max_j) + (i*max_j) + j;
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////
//                                                                                                           //
// This function maps a 3-d array into a 1-d array                                                           //
//                                                                                                           //
///////////////////////////////////////////////////////////////////////////////////////////////////////////////
int map3(int i,int j,int k,int max_i,int max_j,int max_k)
{
    return (k*max_i*max_j) + (i*max_j) + j;
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////
//                                                                                                           //
// This function maps a 2-d array into a 1-d array                                                           //
//                                                                                                           //
///////////////////////////////////////////////////////////////////////////////////////////////////////////////
int map2(int i,int j,int max_i,int max_j)
{
    return (i*max_j) + j;
}

