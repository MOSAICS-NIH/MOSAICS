
///////////////////////////////////////////////////////////////////////////////////////////////////////////////
//                                                                                                           //
// This function initializes a 2-d array of doubles                                                          //
//                                                                                                           //
///////////////////////////////////////////////////////////////////////////////////////////////////////////////
void init_d2array(double ary[][3],int max_i,int max_j)
{
    int i = 0;
    int j = 0;
    for(i=0; i<max_i; i++)
    {
        for(j=0; j<max_j; j++)
        {
            ary[i][j] = 0;
        }
    }
}

